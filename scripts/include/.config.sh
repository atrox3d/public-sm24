#!/bin/bash
################################################################################
#	.config.sh                                                                 #
#	set variables, paths, ecc.                                                 #
#	to be sourced from ALL scripts                                             #
################################################################################
#                                                                              #
#                                                                              #
#	DETERMINE PATHS                                                            #
#                                                                              #
#                                                                              #
################################################################################
#	STARTPATH                                                                  #
#	CURDIR                                                                     #
################################################################################
STARTPATH="$(pwd)"
CURDIR="$(pwd)"
################################################################################
#	SCRIPTNAME                                                                 #
################################################################################
SCRIPTNAME="$(basename $0)"
################################################################################
#	SCRIPTPATH                                                                 #
################################################################################
SCRIPTPATH="$(dirname $(readlink -f $0))"
################################################################################
#	SOURCENAME                                                                 #
################################################################################
SOURCENAME="$(basename $BASH_SOURCE)"
################################################################################
#	SOURCEPATH                                                                 #
################################################################################
SOURCEPATH="$(dirname $(readlink -f $BASH_SOURCE))"
################################################################################
#	BASEEPATH                                                                  #
################################################################################
BASEPATH="$(dirname $(readlink -f "${BASH_SOURCE}/../.."))"
BASEDIR="${BASEPATH}"
################################################################################
#                                                                              #
#                                                                              #
#	CONFIGURE DIRECTORIES                                                      #
#                                                                              #
#                                                                              #
################################################################################
#	SCRIPTDIR                                                                  #
################################################################################
SCRIPTDIR="${BASEDIR}/scripts"
################################################################################
#	INCLUDEPATH                                                                #
#	INCLUDEDIR                                                                 #
################################################################################
INCLUDEPATH="${SCRIPTDIR}/include"
INCLUDEDIR="${INCLUDEPATH}"
################################################################################
#	INCLUDE                                                                    #
################################################################################
INCLUDE="${INCLUDEDIR}/functions.include"
################################################################################
#	DATADIR                                                                    #
################################################################################
#DATADIR="${SCRIPTPATH}/../data/$EMAIL"
#DATADIR="${SCRIPTPATH}/../data"
DATADIR="${BASEDIR}/data"
################################################################################
#	LOGDIR                                                                     #
################################################################################
#LOGDIR="${SCRIPTPATH}/log"
LOGDIR="${SCRIPTDIR}/log"
################################################################################
#	OUTDIR                                                                     #
################################################################################
#OUTDIR="${SCRIPTPATH}/output"
OUTDIR="${SCRIPTDIR}/output"



cd "$SCRIPTDIR"
