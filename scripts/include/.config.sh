#!/bin/bash
################################################################################
#	.config.sh                                                                 #
#	set variables, paths, ecc.                                                 #
#	to be sourced from ALL scripts                                             #
################################################################################
#                                                                              #
#                                                                              #
#	DETERMINE PATHS                                                            #
#                                                                              #
#                                                                              #
################################################################################
#	STARTPATH                                                                  #
#	CURDIR                                                                     #
################################################################################
STARTPATH="$(pwd)"
CURDIR="$(pwd)"
################################################################################
#	SCRIPTNAME                                                                 #
################################################################################
SCRIPTNAME="$(basename $0)"
################################################################################
#	SCRIPTPATH                                                                 #
################################################################################
SCRIPTPATH="$(dirname $(readlink -f $0))"
################################################################################
#	SOURCENAME                                                                 #
################################################################################
SOURCENAME="$(basename $BASH_SOURCE)"
################################################################################
#	SOURCEPATH                                                                 #
################################################################################
SOURCEPATH="$(dirname $(readlink -f $BASH_SOURCE))"
################################################################################
#                                                                              #
#                                                                              #
#	CONFIGURE DIRECTORIES                                                      #
#                                                                              #
#                                                                              #
################################################################################
#	INCLUDEPATH                                                                #
#	INCLUDEDIR                                                                 #
################################################################################
INCLUDEPATH="${SCRIPTPATH}/include"
INCLUDEDIR="${SCRIPTPATH}/include"
################################################################################
#	INCLUDE                                                                    #
################################################################################
INCLUDE="${INCLUDEPATH}/functions.include"
################################################################################
#	DATADIR                                                                    #
################################################################################
DATADIR="${SCRIPTPATH}/../data/$EMAIL"
################################################################################
#	LOGDIR                                                                     #
################################################################################
LOGDIR="${SCRIPTPATH}/log"
################################################################################
#	OUTDIR                                                                     #
################################################################################
OUTDIR="${SCRIPTPATH}/output"



cd "$SCRIPTPATH"
